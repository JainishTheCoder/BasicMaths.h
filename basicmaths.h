inline int product(int x,int y)
{
    return x*y;
}
inline int product(int x,int y,int z)
{
    return x*y*z;
}
inline int product(double x,double y)
{
    return x*y;
}
inline int product(double x,double y,double z)
{
    return x*y*z;
}

inline int sum(int x,int y)
{
    return x+y;
}
inline int sum(int x,int y,int z)
{
    return x+y+z;
}
inline int sum(double x,double y)
{
    return x+y;
}
inline int sum(double x,double y,double z)
{
    return x+y+z;
}

inline int subtraction(int x,int y)
{
    return x-y;
}
inline int subtraction(int x,int y,int z)
{
    return x-y-z;
}
inline int subtraction(double x,double y)
{
    return x-y;
}
inline int subtraction(double x,float y,double z)
{
    return x-y-z;
}

inline int division(int x,int y)
{
    return x/y;
}
inline int division(double x,double y)
{
    return x/y;
}
